/* Question: What skills are required for the top-paying jobs?
-Use the top 10 highest-paying Data Analyst jobs from the first query
-Add the specific skills required for these roles
-Why? It provides a detailed look at which high-paying jobs demand certain skills,
    helping job seekers understand whuch skills to develop that align with top salaries
-Used CTE to run both queries, to find the top paying jobs as well as the
skills for the jobs.
-Used INNER JOIN because we need the skills from the two skills_job_dim and the skills_dim table.
(And job_id in skills_job_dim is connected to job_id in job_postings-fact table and 
skills_dim table connects skill_id with skill_id in skills_job_dim)

*/

WITH top_paying_jobs AS (

    SELECT
        job_id,
        job_title,
        salary_year_avg,
        name AS company_name
    FROM
        job_postings_fact
    LEFT JOIN company_dim ON job_postings_fact.company_id = company_dim.company_id
    WHERE 
        job_title_short = 'Data Analyst' AND
        job_location = 'Anywhere' AND 
        salary_year_avg IS NOT NULL
    ORDER BY 
        salary_year_avg DESC
    LIMIT 10
)

SELECT 
    top_paying_jobs.*,
    skills
FROM top_paying_jobs
INNER JOIN skills_job_dim ON top_paying_jobs.job_id = skills_job_dim.job_id
INNER JOIN skills_dim ON skills_job_dim.skill_id = skills_dim.skill_id
ORDER BY
    salary_year_avg DESC

